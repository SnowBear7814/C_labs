//мой вариант - 15
int main() {
    int a, b, c;
    scanf("%x", &a);
    printf("10-num: %d\n", a);
    printf("8-num: %o\n", a);
    printf("16-num: %x\n", a);
    printf("8-num: %o >> 3 == %o\n", a, a >> 3);
    printf("16-num: %x >> 3 == %x\n", a, a >> 3);
    printf("8-num: ~ %o = %o\n", a, ~a);
    printf("16-num: ~ %x = %x\n", a, ~a);
    b = ('%o\n', a);
    c = ('%x\n', a);
    printf("8-num||16-num: %o||%x == %d\n", a, a,b|c);
    return 0;
}
